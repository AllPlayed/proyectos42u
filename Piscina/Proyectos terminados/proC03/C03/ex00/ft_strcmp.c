int	ft_strcmp(char *s1, char *s2)
{
	int	coun;

	coun = 0;
	while ((s1[coun] == s2[coun]) && (s1[coun] != '\0') && (s2[coun] != '\0'))
		coun++;
	return (s1[coun] - s2[coun]);
}
